    private void cSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cSimpanActionPerformed
        matkul = tMatkul.getText();
        deadline = tDeadline.getText();
        detail = tDetail.getText();

        if (matkul.isEmpty() || deadline.isEmpty() || detail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukan Data Dengan Benar!! dan isi Semuanya");
        }else {
        list1.addElement(angka + "." + matkul + " (DL : " + deadline + ")");
        list2.addElement(detail);
        
        tMatkul.setText("");
        tDeadline.setText("");
        tDetail.setText("");
        
         JOptionPane.showMessageDialog(this,"Data Berhasil diSimpan");
         angka++;
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_cSimpanActionPerformed

