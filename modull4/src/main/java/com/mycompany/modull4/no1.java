    private void jListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListMouseClicked
        if (evt.getClickCount() == 1) {
            int pilihIndex = jList.locationToIndex(evt.getPoint());
        if (pilihIndex >= 0) {
           // Mendapatkan nilai dari elemen yang dipilih
           String pilihdetail = list2.getElementAt(pilihIndex);
           jTugas.setText(pilihdetail);
       }
   }
        // TODO add your handling code here:
    }//GEN-LAST:event_jListMouseClicked

    private void jHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jHapusActionPerformed

         int selectedIndex = jList.getSelectedIndex();
    
        if (selectedIndex >= 0) {
            list1.remove(selectedIndex);
            jTugas.setText("");
        } else {
        JOptionPane.showMessageDialog(this, "Pilih list tugas untuk dihapus");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jHapusActionPerformed

